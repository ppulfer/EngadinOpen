async function postScore(player, date, holes) {
  const res = await fetch(`${API}/api/scores?year=2022`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ player, date, holes }),
  });
  if (!res.ok) throw new Error(`POST ${player}/${date} -> ${res.status}`);
  return res.json();
}

(async () => {
  const summary = [];
  for (const [player, byDate] of Object.entries(DATA)) {
    let grand = 0;
    for (const [date, raw] of Object.entries(byDate)) {
      const holes = raw.map((h) =>
        h === null
          ? { strokes: null, putts: null, lostBalls: 0 }
          : { strokes: h[0], putts: h[1], lostBalls: 0 }
      );
      const total = holes.reduce((s, h) => s + (h.strokes || 0), 0);
      grand += total;
      await postScore(player, date, holes);
      process.stdout.write(`  ${player.padEnd(12)} ${date}: ${total}\n`);
    }
    summary.push({ player, total: grand });
  }
  console.log('\n=== Brutto totals ===');
  summary.sort((a, b) => a.total - b.total);
  summary.forEach((s, i) => console.log(`${i + 1}. ${s.player.padEnd(12)} ${s.total}`));
})();
