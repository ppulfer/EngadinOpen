async function postScore(player, date, holes) {
  const res = await fetch(`${API}/api/scores?year=2025`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ player, date, holes }),
  });
  if (!res.ok) throw new Error(`POST ${player}/${date} -> ${res.status}`);
  return res.json();
}

async function deleteParliStats() {
  for (const date of ROUND_DATES) {
    const res = await fetch(`${API}/api/admin?year=2025`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ action: 'delete', key: `parli__score__${date}` }),
    });
    if (!res.ok) throw new Error(`delete parli/${date} -> ${res.status}`);
  }
}

(async () => {
  const summary = [];
  for (const [player, byDate] of Object.entries(DATA)) {
    let grand = 0;
    for (const date of ROUND_DATES) {
      const holes = byDate[date].map(([strokes, putts]) => ({ strokes, putts, lostBalls: 0 }));
      const total = holes.reduce((s, h) => s + h.strokes, 0);
      grand += total;
      await postScore(player, date, holes);
      process.stdout.write(`  ${player.padEnd(12)} ${date}: ${total}\n`);
    }
    summary.push({ player, total: grand });
  }
  await deleteParliStats();
  console.log('\n=== Brutto totals ===');
  summary.sort((a, b) => a.total - b.total);
  summary.forEach((s, i) => console.log(`${i + 1}. ${s.player.padEnd(12)} ${s.total}`));
  console.log('\nParli stats removed (Parli did not play 2025).');
})();
